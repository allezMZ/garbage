// 1.数组的首尾增删
let a =[5,4,2,78,15,56]

a.push(100)  //尾部添加
console.log('尾部添加',a);
a.pop()  //尾部删除
console.log('尾部删除',a);
a.unshift(99)  //首部添加
console.log('首部添加',a);
a.shift() //首部删除
console.log('首部删除',a)
// [5, 4, 2, 78, 15, 56,100]
// [5, 4, 2, 78, 15, 56]
// [99,5, 4, 2, 78, 15, 56]
// [5, 4, 2, 78, 15, 56]

